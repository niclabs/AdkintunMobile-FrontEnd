def remove_accent(letter):
    if letter =="á":
        return "a"
    elif letter == "Á":
        return "A"
    elif letter == "é":
        return "e"
    elif letter == "É":
        return "E"
    elif letter == "í":
        return "i"
    elif letter == "Í":
        return "I"
    elif letter == "ó":
        return "o"
    elif letter == "Ó":
        return "O"
    elif letter == "ú":
        return "u"
    elif letter == "Ú":
        return "U"
    elif letter == "ñ":
        return "n"
    elif letter == "Ñ":
        return "N"
    else:
        return letter

def uniform(word):
    if word == "Lo Abarca":
        return "Cartagena"
    elif word == "San Vicente de Tagua Tagua":
        return "San Vicente"
    else:
        return ''.join(map(remove_accent, word))

